.Last <-
function () 
{
    if (interactive()) {
        hist_file <- Sys.getenv("R_HISTFILE")
        if (hist_file == "") 
            hist_file <- "~/.Rhistory"
        savehistory(hist_file)
        file.append("~/.Rhistory_all", hist_file)
    }
}
